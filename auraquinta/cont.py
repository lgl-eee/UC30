dados = [10, 20, 30, 60, 30]
print(dados)

#numero de elementos
print("Comprimento: ", len(dados))

# conta ocorrencias
print("quantas vezes o numero 30 aparece: ", dados.count(30))

#index - encontrar posição
print("posição do numero 20: ", dados.index(20))

# in - verificar existencia
print("o numero 20 existe na lista? ", 20 in dados)
print("o numero 100 existe na lista? ", 100 in dados)